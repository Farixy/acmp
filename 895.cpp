#include <iostream>
using namespace std;
int main() {
	char a[3][3]; int flag = 2;
	for (int i = 0; i < 3;i++)
	for (int j = 0; j < 3;j++) {
	cin>>a[i][j];
	}
	if ((a[0][0]=='X') && (a[0][1]=='X') && (a[0][2]=='X')) flag = 0; if ((a[0][0]=='O') && (a[0][1]=='O') && (a[0][2]=='O')) flag = 1;
	if ((a[1][0]=='X') && (a[1][1]=='X') && (a[1][2]=='X')) flag = 0; if ((a[1][0]=='O') && (a[1][1]=='O') && (a[1][2]=='O')) flag = 1;
	if ((a[2][0]=='X') && (a[2][1]=='X') && (a[2][2]=='X')) flag = 0; if ((a[2][0]=='O') && (a[2][1]=='O') && (a[2][2]=='O')) flag = 1;
	if ((a[0][0]=='X') && (a[1][0]=='X') && (a[2][0]=='X')) flag = 0; if ((a[0][0]=='O') && (a[1][0]=='O') && (a[2][0]=='O')) flag = 1;
	if ((a[0][1]=='X') && (a[1][1]=='X') && (a[2][1]=='X')) flag = 0; if ((a[0][1]=='O') && (a[1][1]=='O') && (a[2][1]=='O')) flag = 1;
	if ((a[0][2]=='X') && (a[1][2]=='X') && (a[2][2]=='X')) flag = 0; if ((a[0][2]=='O') && (a[1][2]=='O') && (a[2][2]=='O')) flag = 1;
	if ((a[0][0]=='X') && (a[1][1]=='X') && (a[2][2]=='X')) flag = 0; if ((a[0][0]=='O') && (a[1][1]=='O') && (a[2][2]=='O')) flag = 1;
	if ((a[0][2]=='X') && (a[1][1]=='X') && (a[2][0]=='X')) flag = 0; if ((a[0][2]=='O') && (a[1][1]=='O') && (a[2][0]=='O')) flag = 1;
	if (flag==2) {cout<<"Draw"; return 0;}
	if (flag==1) {cout<<"Lose"; return 0;}
	if (flag==0) {cout<<"Win"; return 0;}	

	return 0;
}